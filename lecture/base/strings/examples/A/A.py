# Задача:
# Напишите функцию, которая проверяет, является ли данная строка палиндромом (читается одинаково с начала и с конца).


def is_palindrome(s):
    s = s.lower().replace(" ", "")  # Приводим к нижнему регистру и удаляем пробелы
    return s == s[::-1]  # Сравниваем строку с её обратным вариантом

# Пример использования
input_str = "A man a plan a canal Panama"
result = is_palindrome(input_str)
print(result)  # Возвращает True